package day05_variables;

public class ConateExample {

    public static void main(String[] args) {

        int numberOne = 4, numberTwo = 5;

        System.out.println(numberOne + numberTwo);
        System.out.println(4 + 5);
        System.out.println("4" + "5");
        System.out.println('4' + '5');

        System.out.println();

    }
}
