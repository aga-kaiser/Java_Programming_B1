package day3_comments_escape_sequence;

public class Quote {

    public static void main (String[] args) {

        // The bellow code prints this quote -> I like "Java" programing.
        System.out.println("I like \"Java\" programing");

        // Can you print " for me?
        System.out.println("\"");


        // How can I print the backslash ---> itself?
        System.out.println("\\");

        // To print out two backslash
        System.out.println("\\\\");

        // Can you print ' for me?
        System.out.println("\'");


        System.out.println("/");








    }
}
